#!/usr/bin/python
import argparse
import os,sys,inspect
currentdir = os.path.dirname(os.path.abspath(inspect.getfile(inspect.currentframe())))
parentdir = os.path.dirname(currentdir)
sys.path.insert(0,parentdir) 



from vhdl_build_system.vhdl_build_system.vhdl_make_test_bench import make_test_bench_main



def main():
    parser = argparse.ArgumentParser(description='Creates Test benches for a given entity')
    parser.add_argument('--OutputPath', help='Path to where the test bench should be created',default="session_08/test/")
    parser.add_argument('--EntityName', help='Name of the entity Test bench',default="entiy_with_iop_tb")
    parser.add_argument('--NumberOfRows', help='Number of Rows in the Autogenerated CSV Files',default="1000")
    args = parser.parse_args()

    
    make_test_bench_main(args.EntityName,args.NumberOfRows, args.OutputPath)
    


if __name__== "__main__":
    main()



    