#!/usr/bin/python
import argparse
import os,sys,inspect
currentdir = os.path.dirname(os.path.abspath(inspect.getfile(inspect.currentframe())))
parentdir = os.path.dirname(currentdir)
sys.path.insert(0,parentdir) 

from  vhdl_build_system.vhdl_parser                import *
from  vhdl_build_system.vhdl_get_dependencies      import *
from  vhdl_build_system.vhdl_get_entity_def        import *
from  vhdl_build_system.vhdl_get_type_def_from_db  import *
from  vhdl_build_system.vhdl_make_stand_alone_impl import *
from  vhdl_build_system.vhdl_make_stand_alone_impl import *
from  vhdl_build_system.vhdl_make_test_bench       import *

def main():
    parser = argparse.ArgumentParser(description='Creates Test benches for a given entity')
    parser.add_argument('--OutputPath', help='Path to where the test bench should be created',default="klm_scint/tests/tb_fifo_cc_axi")
    parser.add_argument('--EntityName', help='Name of the entity Test bench',default="fifo_cc_axi")
    parser.add_argument('--NumberOfRows', help='Number of Rows in the Autogenerated CSV Files',default="1000")

    args = parser.parse_args()
    
    cwd = os.getcwd()
    print(cwd)
    InputFile = entity2FileName(args.EntityName)
    NumberOfRows = int(args.NumberOfRows)
    entityDef = vhdl_get_entity_def(InputFile)
    ParsedFile = vhdl_parser(InputFile)
    make_package_file(entityDef,"none","write",ParsedFile["packageUSE"], args.OutputPath)
    make_package_file(entityDef,"out","read",ParsedFile["packageUSE"], args.OutputPath)

    make_read_entity(entityDef,args.OutputPath)
    make_write_entity(entityDef,args.OutputPath)
    make_test_bench_for_test_cases(entityDef,args.OutputPath)
    sim_in_filename = args.OutputPath+"/"+get_test_bench_file_basename(entityDef)+".csv"
    
    make_sim_csv_file(entityDef,sim_in_filename,"out",NrOfEntires=NumberOfRows)
    sim_out_filename = args.OutputPath+"/"+get_test_bench_file_basename(entityDef)+"_out.csv"
    make_sim_csv_file(entityDef,sim_out_filename,"none",NrOfEntires=NumberOfRows)
    
    make_xml_test_case(entityDef,args.OutputPath)
    make_stand_alone_impl( entityDef = entityDef , path = args.OutputPath, suffix= "")


if __name__== "__main__":
    main()